SELECT
       STUDENT_NO
     , STUDENT_NAME
     , DEPARTMENT_NAME
    FROM TB_STUDENT 
    JOIN TB_DEPARTMENT USING(DEPARTMENT_NO)
    ORDER BY 2;
    
-- 오라클 전용 구문
SELECT
       STUDENT_NO
     , STUDENT_NAME
     , DEPARTMENT_NAME
    FROM TB_DEPARTMENT D, TB_STUDENT S
 WHERE D.DEPARTMENT_NO = S.DEPARTMENT_NO
 ORDER BY 2;
 
